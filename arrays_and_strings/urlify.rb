# Write a method to replace all spaces in a string with a '%20'. You may assume that the string has sufficient space at the end to hold the addition characters, and that you are given the "true" length of the string.
# input: "Mr John Smith"
# output: "Mr%20John%20Smith"

def urlify(string)
	if string.include?(' ') == true
		string.gsub!(' ', '%20')
	else
		string
	end
end
